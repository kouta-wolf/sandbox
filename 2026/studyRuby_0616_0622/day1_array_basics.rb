items = ["剣", "盾", "ポーション"]



# ここに実装する
# 2.
items.push("エリクサー")
p items #=> ["剣", "盾", "ポーション", "エリクサー"]

# 3.push pop shift unshiftの使い方がそれぞれ曖昧だなって思った
items.shift
p items #=> ["盾", "ポーション", "エリクサー"]

# 4. 配列は基本先頭が0番目。ただ日本語的には最初は1番目になるため2番めは[1]になる
# と思ったらインデックスで、と書いてあるので素直に[2]でいいのかな？
