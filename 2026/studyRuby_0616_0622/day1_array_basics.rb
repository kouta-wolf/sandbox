# 5.確かlengthとsizeがあったよね…どう使い分けるのか？
puts items.size

# 6.検索をかける…とするならfilterとかだっけ
# 素直に該当すればそこで止めないと全探索を起こす。あとブロックで書きますのでfindです
# ただこれってtrue falseをどうすればいいんだっけ…わかんなくなったので条件分岐しますか…後で教えて
# こうやったけどなぜか2つ出る…なぜ？
