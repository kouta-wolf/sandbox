# =============================================================
# Day 1（月）— クラスとinitialize
# テーマ：キャラクターの基本情報を持つクラスを一から書く
# =============================================================
# 【課題】
#   name, hp, mp を受け取って保持する Character クラスを完成させる
#
# 【確認ポイント】
#   - attr_reader と attr_accessor の違いを説明できるか
#   - initialize の引数と @ の関係を口頭で言えるか
# =============================================================

class Character

  # 個人的にattrとinitializeは逆順だが説明のためにあえてこのように
  # オブジェクトに初期値を与えることができるのがinitializeメソッド。
  # 引数も外から持ってこれるようにもできる
  # class内でその引数を利用するためにはその引数を@をつけてインスタンス変数にすることで
  # classの中で持ってきた引数を利用することが可能になる
  def initialize(name, hp, mp)
    @name = name
    @hp = hp
    @mp = mp
  end

  # 本来Classの外からではこれらの引数を読んだり書き換えたりすることはできない
  # そこでattr_から始まる機能を使うことで外から確認可能になる
  # attr_のあとのreader(読み込み専用)やwriter(書き込み専用）,accessor(読み書き両用)
  # これをすることで設定した変数の中身を呼び出せる出入り口になる
  attr_reader :name, :hp, :mp

end

# あとはクラスの外でオブジェクトがnewメソッドを呼び出し、引数を渡してインスタンスを作成
# attr_readerに設定しているためこれらを読み出すことができる
hero = Character.new("アリス", 100, 50)
puts hero.name  # => アリス
puts hero.hp    # => 100
puts hero.mp    # => 50
