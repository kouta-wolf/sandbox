# -------------------------------------------------------------
# STEP 1: 知っている状態の確認（設計図とインスタンスメソッド）
# -------------------------------------------------------------
# 問題点: このクラスは「スライム」固定。
#         別のモンスターを出したいとき、どうすればいい?
class MonsterV1
  def appearance
    puts "スライムが現れた!!"
  end
end

m1 = MonsterV1.new
m1.appearance
# => スライムが現れた!!


# -------------------------------------------------------------
# STEP 2: initialize（コンストラクタ）
# -------------------------------------------------------------
# new を呼ぶと自動で実行される特殊メソッド。
# new に渡した引数をここで受け取れる。
# → 作る時点でデータを渡せるようになる
class MonsterV2
  def initialize(name)
    puts "#{name}が現れた!!"
  end
end

m2 = MonsterV2.new("ドラゴン")
# => ドラゴンが現れた!!


# -------------------------------------------------------------
# STEP 3: インスタンス変数 @
# -------------------------------------------------------------
# initialize で受け取った値を「そのオブジェクトの中に保存」したい。
# ローカル変数はメソッドが終わると消える → @変数 を使う。
#
# @変数 の特徴:
#   - 同じオブジェクトの中ならどのメソッドからでも読み書きできる
#   - オブジェクトごとに独立した値を持つ（m3とm4は別々の@name）
class MonsterV3
  def initialize(name, hp)
    @name = name   # このオブジェクトの名前を保存
    @hp   = hp     # このオブジェクトのHPを保存
  end

  def appearance
    puts "#{@name}が現れた!! HP: #{@hp}"
  end

  def damaged(amount)
    @hp -= amount
    puts "#{@name}は#{amount}のダメージ! 残りHP: #{@hp}"
  end
end

m3 = MonsterV3.new("スライム", 30)
m4 = MonsterV3.new("ドラゴン", 200)

m3.appearance
m4.appearance
m3.damaged(10)
m4.damaged(50)
m3.appearance   # m3のHPだけ変わっている（m4は独立）


# -------------------------------------------------------------
# STEP 4: アクセサ（attr_reader / attr_writer / attr_accessor）
# -------------------------------------------------------------
# 問題: @name や @hp をクラスの外から直接読み書きしたい。
# でも @変数 はクラスの外からは触れない。
#
#   puts m3.name  # → エラー！ NoMethodError
#
# 解決策: ゲッター（読む用）・セッター（書く用）メソッドを作る。
# 手で書くとこうなる:
#
#   def name        # ゲッター（読む）
#     @name
#   end
#   def name=(val)  # セッター（書く）
#     @name = val
#   end
#
# これを1行で自動生成してくれるのが attr_* メソッド。
#
#   attr_reader   :name       → ゲッターだけ（外から読める・書けない）
#   attr_writer   :name       → セッターだけ（外から書ける・読めない）
#   attr_accessor :name       → 両方（読める・書ける）

class MonsterV4
  attr_reader   :name   # 外から読める、書けない
  attr_accessor :hp     # 外から読める、書ける

  def initialize(name, hp)
    @name = name
    @hp   = hp
  end

  def appearance
    puts "#{@name}が現れた!! HP: #{@hp}"
  end
end

m5 = MonsterV4.new("ゴブリン", 50)

puts m5.name        # "ゴブリン"（attr_reader で読める）
puts m5.hp          # 50（attr_accessor で読める）

m5.hp = 30          # attr_accessor で書ける
puts m5.hp          # 30

# m5.name = "オーク" # → エラー！ attr_reader なので書けない


# -------------------------------------------------------------
# STEP 5: クラス変数 @@
# -------------------------------------------------------------
# @変数  → そのオブジェクト専用
# @@変数 → クラス全体（全オブジェクト）で共有される1つの値
#
# 使いどころ: 「何体モンスターを作ったか」など全体で管理したい値
class MonsterV5
  attr_reader :name, :id

  @@count = 0   # クラス全体で1つだけ存在。0からスタート

  def initialize(name)
    @@count += 1   # newするたびに全体のカウントが増える
    @id   = @@count
    @name = name
  end

  def info
    "No.#{@id} #{@name}"
  end
end

ma = MonsterV5.new("スライム")
mb = MonsterV5.new("ゴブリン")
mc = MonsterV5.new("ドラゴン")

puts ma.info   # "No.1 スライム"
puts mb.info   # "No.2 ゴブリン"
puts mc.info   # "No.3 ドラゴン"
# @id はオブジェクトごとに別々、@@count は3つのオブジェクトで共有


# -------------------------------------------------------------
# STEP 6: クラスメソッド（self.メソッド名）
# -------------------------------------------------------------
# これまでのメソッドは「オブジェクトを作ってから呼ぶ」インスタンスメソッド。
# クラスメソッドは「オブジェクトを作らずにクラス自体に対して呼ぶ」。
#
#   Monster.count  のように呼べる（Monster.new は不要）
#
# 定義方法: def self.メソッド名
# 使いどころ: @@変数の参照、ファクトリメソッド など
class MonsterV6
  attr_reader :name, :id

  @@count = 0

  def initialize(name)
    @@count += 1
    @id   = @@count
    @name = name
  end

  def info
    "No.#{@id} #{@name}"
  end

  # クラスメソッド: オブジェクト不要で呼べる
  def self.count
    @@count
  end
end

MonsterV6.new("スライム")
MonsterV6.new("ゴブリン")

puts MonsterV6.count   # 2（MonsterV6.new せずに呼べる）


# =============================================================
# まとめ
# =============================================================
# @変数         オブジェクトごとの値。同じクラスの全メソッドで使える
# @@変数        クラス全体（全オブジェクト）で共有する値
# attr_reader   @変数を外から「読む」だけOKにする
# attr_writer   @変数を外から「書く」だけOKにする
# attr_accessor @変数を外から「読み書き」両方OKにする
# self.メソッド  オブジェクトを作らずクラス自体に呼ぶクラスメソッド
# =============================================================
